// Automatic Variables
#include <iostream>
int main()
{
    auto x = 1;
    auto y = 1.5f;
    auto z = 2.2;
    auto c = 'c';
    auto s = "My Name is Moutaz";

    std::cout << x << "\n";
    std::cout << y << "\n";
    std::cout << z << "\n";
    std::cout << c << "\n";
    std::cout << s << "\n";
}

